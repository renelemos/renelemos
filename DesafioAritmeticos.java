package fundamentos;

public class DesafioAritmeticos {

	public static void main(String[] args) {
		
		double a = 3 + 2;
		double b = (1 - 5);
		double c = (2 - 7);
		double d = 3 * 2;
		
		double e = Math.pow((6*a),2);
		double f = e/d;
		double g = Math.pow((b * c)/2,2);
		
		double h = Math.pow((f-g), 3);
		double i = Math.pow(10, 3);
		double j = h/i;
		
		System.out.println(j);
	
	
		
	
	
	}
}
